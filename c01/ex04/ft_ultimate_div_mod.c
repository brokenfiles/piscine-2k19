void	ft_ultimate_div_mod(int *a, int *b)
{
	int	cp_a;
	int	cp_b;

	cp_a = *a;
	cp_b = *b;
	if (cp_b != 0)
	{
		*a = cp_a / cp_b;
		*b = cp_a % cp_b;
	}
}
