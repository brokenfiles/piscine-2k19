int        ft_is_prime(int nb)
{
	int i;

	if ((nb % 2 == 0 && nb != 2) || nb <= 1)
		return (0);
	i = 3;
	while (i * i <= nb && i < 46349)
	{
		if ((nb % i) == 0)
			return (0);
		i = i + 2;
	}
	return (1);
}

int	main()
{
	ft_is_prime(1);
}
