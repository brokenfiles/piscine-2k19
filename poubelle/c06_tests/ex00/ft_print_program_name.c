#include <unistd.h>

void	ft_putstr(char *str)
{
	int	index;

	index = 0;
	while (str[index])
	{
		write(1, &str[index], 1);
		index++;
	}
}

int main(int argc,  char **argv)
{
	if (argv > 0)
	{
		ft_putstr(argv[0]);
	}
	ft_putstr("\n");
	return (0);
}
