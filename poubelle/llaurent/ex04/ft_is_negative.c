#include <unistd.h>

void	ft_is_negative(int n)
{
	char	chars[2];

	chars[0] = 'P';
	chars[1] = 'N';
	if (n >= 0)
	{
		write(1, &chars[0], 1);
	}
	else
	{
		write(1, &chars[1], 1);
	}
}
