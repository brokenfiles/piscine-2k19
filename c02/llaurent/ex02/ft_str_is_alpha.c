int	ft_str_is_alpha(char *str)
{
	int	result;
	int	i;
	int	c;

	result = 0;
	i = 0;
	while (str[i])
	{
		c = str[i];
		if (
				(c >= 65 && c <= 90) ||
				(c >= 97 && c <= 122))
		{
			result++;
		}
		i++;
	}
	return (result == i);
}
