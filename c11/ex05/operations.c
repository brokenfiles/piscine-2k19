int	plus(int a, int b)
{
	return (a + b);
}

int	less(int a, int b)
{
	return (a - b);
}

int	divide(int a, int b)
{
	return (a / b);
}

int	multiply(int a, int b)
{
	return (a * b);
}

int	modulo(int a, int b)
{
	return (a % b);
}
